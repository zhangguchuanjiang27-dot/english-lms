export const GRAMMAR_CATEGORIES: Record<string, GrammarCategory[]> = {};

// Parse RAW_GRAMMAR_DATA into GRAMMAR_CATEGORIES
for (const level in RAW_GRAMMAR_DATA) {
    GRAMMAR_CATEGORIES[level] = RAW_GRAMMAR_DATA[level].map(category => {
        const questions = category.rawQuestions.map(line => {
            const [translation, answerStr] = line.split('|').map(s => s.trim());
            // Preprocess strings that missed a space before ? or .
            const sanitizedAnswer = answerStr.replace(/([.?\!,])/g, ' $1').replace(/\s+/g, ' ').trim();

            const answer = sanitizedAnswer.split(' ').filter(w => w.length > 0);

            // ちゃんとランダムになるよう Fisher-Yates アルゴリズム風 または ランダムマップでソート
            const words = answer
                .map(value => ({ value, sort: Math.random() }))
                .sort((a, b) => a.sort - b.sort)
                .map(({ value }) => value);

            return {
                translation,
                words,
                answer
            };
        });

        return {
            id: category.id,
            title: category.title,
            description: category.description,
            questions
        };
    });
}
